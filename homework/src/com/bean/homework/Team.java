package com.bean.homework;

public class Team
{
	private String teamId;
	private String teamName;
	private String teamCour;
	private String teamTeacherid;
	private String teamMonitorid;
	private String teamMonitor;
	private String teamMember;
	
	

	public Team(){}

	public String getTeamId() {
		return teamId;
	}

	public void setTeamId(String teamId) {
		this.teamId = teamId;
	}

	public String getTeamName() {
		return teamName;
	}

	public void setTeamName(String teamName) {
		this.teamName = teamName;
	}

	public String getTeamCour() {
		return teamCour;
	}

	public void setTeamCour(String teamCour) {
		this.teamCour = teamCour;
	}

	public String getTeamMonitorid() {
		return teamMonitorid;
	}

	public void setTeamMonitorid(String teamMonitorid) {
		this.teamMonitorid = teamMonitorid;
	}

	public String getTeamMonitor() {
		return teamMonitor;
	}

	public void setTeamMonitor(String teamMonitor) {
		this.teamMonitor = teamMonitor;
	}

	public String getTeamTeacherid() {
		return teamTeacherid;
	}

	public void setTeamTeacherid(String teamTeacherid) {
		this.teamTeacherid = teamTeacherid;
	}
	public String getTeamMember() {
		return teamMember;
	}

	public void setTeamMember(String teamMember) {
		this.teamMember = teamMember;
	}
   
	
	
}